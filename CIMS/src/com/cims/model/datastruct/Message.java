package com.cims.model.datastruct;

public class Message {
	private String msg;
	private boolean state;
	
	
	public String getMsg() {
		return msg;
	}
	public boolean isState() {
		return state;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}
	public void setState(boolean state) {
		this.state = state;
	}
	
	
}
